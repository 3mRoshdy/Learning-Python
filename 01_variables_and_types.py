# Numbers
myInt = 3
myFloat = 1.0
mySecondFloat = float(myInt)

print(myInt)
print(myFloat)
print(mySecondFloat)

# Strings
myString = 'cares \'bout apostrophes'
mySeondString = "No caring 'bout apostrophes"

print(myString)
print(mySeondString)

# Multiple assigning
a, b, c = 1, 2, 3
print(a,b,c)

# Printing variables based on types
print("String --> %s" % myString)
print("Int --> %d" % myInt)
print("Float --> %f" % myFloat)
